def write_dataset_cards(output_dir: Path = Path("docs/tables")) -> dict[str, str]:
    output_dir.mkdir(parents=True, exist_ok=True)
    audit_rows: list[dict[str, Any]] = []
    outputs: dict[str, str] = {}
    for spec in DATASETS:
        dataset_dir = spec["dataset_dir"]
        audit = audit_dataset(dataset_dir) if dataset_dir.exists() else _missing_audit(dataset_dir)
        _write_card(spec, audit)
        audit_rows.append(_audit_row(spec, audit))
        outputs[spec["name"]] = str(spec["card"])
    csv_path = output_dir / "provider_view_audit_summary.csv"
    md_path = output_dir / "provider_view_audit_summary.md"
    write_csv(csv_path, audit_rows)
    _write_markdown(md_path, audit_rows)
    outputs["provider_view_audit_summary"] = str(csv_path)
    return outputs


def _missing_audit(dataset_dir: Path) -> dict[str, Any]:
    return {
        "dataset_dir": str(dataset_dir),
        "requests": 0,
        "truth_rows": 0,
        "workflow_count": 0,
        "project_count": 0,
        "org_count": 0,
        "user_count_excluding_missing": 0,
        "non_provider_attack_view_fields": "missing_dataset",
        "non_provider_provider_metadata_fields": "missing_dataset",
        "repair_modes": [],
        "repair_fields": [],
        "leakage_markers": [],
        "source_manifest": None,
    }


def _write_card(spec: dict[str, Any], audit: dict[str, Any]) -> None:
    path = spec["card"]
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        f"# Dataset Card: {spec['name']}",
        "",
        "## Source",
        "",
        spec["source"],
        "",
        "## Intended Use",
        "",
        spec["claim_scope"],
        "",
        "## Provider-View Fields",
        "",
        spec["provider_view_note"],
        "",
        "Allowed attack-view fields are `request_id`, `timestamp`, `model`, `messages`, `tool_schemas`, `token_count`, `cache_bucket`, and restricted `provider_metadata` keys.",
        "",
        "## Ground Truth",
        "",
        spec["ground_truth_note"],
        "",
        "## Audit Summary",
        "",
        f"- Dataset directory: `{audit['dataset_dir']}`",
        f"- Requests: {audit['requests']}",
        f"- Truth rows: {audit['truth_rows']}",
        f"- Workflows: {audit['workflow_count']}",
        f"- Projects: {audit['project_count']}",
        f"- Owner/org-like labels: {audit['org_count']}",
        f"- Users with reliable labels: {audit['user_count_excluding_missing']}",
        f"- Non-provider attack-view fields: `{audit['non_provider_attack_view_fields']}`",
        f"- Non-provider provider_metadata fields: `{audit['non_provider_provider_metadata_fields']}`",
        f"- Repair modes: `{audit['repair_modes']}`",
        f"- Repair fields: `{audit['repair_fields']}`",
        f"- Leakage markers: `{audit['leakage_markers']}`",
        "",
        "## License And Ethics",
        "",
        spec["license_note"],
        "",
        "Raw prompt content can include code, logs, paths, usernames, repository names, and operational clues. Paper artifacts should prefer aggregate metrics, redacted snippets, and derived tables.",
        "",
        "## Limitations",
        "",
        "- Provider-view logs are approximations of what a model API provider can observe, not an exact commercial API schema.",
        "- Timestamps may be repaired or synthetic when the upstream source lacks provider-log timing.",
        "- Owner/org-like labels derived from repository owners must not be interpreted as enterprise organizations.",
        "- User-level reconstruction is N/A when reliable user labels are unavailable.",
        "",
    ]
    path.write_text("\n".join(lines), encoding="utf-8")


def _audit_row(spec: dict[str, Any], audit: dict[str, Any]) -> dict[str, Any]:
    return {
        "dataset": spec["name"],
        "dataset_dir": str(spec["dataset_dir"]),
        "requests": audit["requests"],
        "truth_rows": audit["truth_rows"],
        "workflows": audit["workflow_count"],
        "projects": audit["project_count"],
        "owner_org_like_labels": audit["org_count"],
        "users_with_labels": audit["user_count_excluding_missing"],
        "non_provider_attack_view_fields": audit["non_provider_attack_view_fields"],
        "non_provider_provider_metadata_fields": audit["non_provider_provider_metadata_fields"],
        "repair_modes": audit["repair_modes"],
    }


def _write_markdown(path: Path, rows: list[dict[str, Any]]) -> None:
    headers = list(rows[0]) if rows else []
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(str(row.get(header)) for header in headers) + " |")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Write dataset cards and provider-view audit summary.")
    parser.add_argument("--output-dir", type=Path, default=Path("docs/tables"))
    args = parser.parse_args()
    print(write_dataset_cards(args.output_dir))


if __name__ == "__main__":
    main()
